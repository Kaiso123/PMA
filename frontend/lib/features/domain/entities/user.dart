class User {
  final int? userId;
  final String? username;
  final String? password;
  final String? email;
  final int? age;
  final String? address;
  final String? gender;
  final int? phone;
  final String? name;

  const User({
    this.userId,
    this.username,
    this.password,
    this.email,
    this.age,
    this.address,
    this.gender,
    this.phone,
    this.name,
  });
}